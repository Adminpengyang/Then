package com.cssl.pojo;

public class Jiedao {

	private int id;
	private String jname;
	private int guishu;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJname() {
		return jname;
	}
	public void setJname(String jname) {
		this.jname = jname;
	}
	public int getGuishu() {
		return guishu;
	}
	public void setGuishu(int guishu) {
		this.guishu = guishu;
	}
	
	
}
